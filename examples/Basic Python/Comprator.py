# Python Comparison operators
""""
== --> Value Equality
!= --> Not equal to
< --> Less than
> --> Greater than
<= --> Less than or equal to
>= --> Greater than or equal to
"""

boolOne = 10 == 11
notEqual = 10 != 11
lessThan = 10 < 11
greaterThan = 10 > 9
lessThanEQ = 10 <= 10
greaterThanEq = 10 >= 11 - 1
print(lessThanEQ)
print(greaterThanEq)
